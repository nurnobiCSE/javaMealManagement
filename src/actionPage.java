      String name = jTextField1.getText();
      String mobile = jTextField2.getText();
      String email = jTextField3.getText();
      String nid = jTextField4.getText();
      String profession = jTextField5.getText();
      String fatherName = jTextField6.getText();
      String city = jTextField7.getText();
      String age = jTextField8.getText();
      String religion = jTextField9.getText();
