ps.setString(2, mobile);
ps.setString(3, email);
ps.setString(4, nid);
ps.setString(5, profession);
ps.setString(6, fatherName);
ps.setString(7, city);
ps.setString(8, age);
ps.setString(9, religion);
ps.setString(10, id);
ps.executeUpdate();
